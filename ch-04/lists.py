friends = ["apple", "Orange", 5, 7.7, False]
#friends is a list and in a list we can store datatypes of any type together
#lists are muttable so it means we can change the value
print(friends)
print(friends[0])           #here the first term was apple
friends[0]= "mango"        #but here we changed the value from apple to mango
print(friends[0])           #so now it will give us the value as mango


#List slicing is the same as the slicing in the strings
print(friends[0:4])

